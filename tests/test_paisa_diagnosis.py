"""Unit tests for Paisa v0.7.4 diagnosis classification (contra-expense
``Debit Entry`` dangers the projection provably generates vs. everything else).

Covers the contract required by the live-sync integration:
* legitimate generated refund/cashback/fee-reversal ``Debit Entry`` dangers are
  *accepted* (downgraded) via exact ``(account, date, amount)`` fingerprint
  matching against the typed ProjectionReport;
* an extra same-account/date/amount danger beyond the expected multiplicity
  stays fatal;
* an unmatched ``Debit Entry`` (operator-authored content), any other expense
  debit not generated by us, a ``Negative Balance``, and a malformed/unparseable
  issue all stay fatal;
* matching is anchored on Paisa's real HTML template (no blanket string
  suppression) and the Go ``02 Jan 2006`` date format.
"""

import datetime
from collections import Counter
from decimal import Decimal

import pytest

from financial_dashboard.integrations.paisa import PaisaDiagnosis, PaisaDiagnosisIssue
from financial_dashboard.services.paisa.diagnosis import (
    DEBIT_ENTRY_SUMMARY,
    DebitEntryFingerprint,
    classify_diagnosis,
    expected_debit_entry_fingerprints,
    parse_debit_entry_details,
)
from financial_dashboard.services.paisa.renderers.base import (
    LedgerPosting,
    ProjectedEntry,
)

CUTOVER = datetime.date(2026, 1, 1)


# ---------------------------------------------------------------------------
# Fixtures / helpers
# ---------------------------------------------------------------------------


def _entry(
    *,
    contra_account,
    contra_amount,
    date,
    kind="expense",
    bank_account="Assets:Bank:Hdfc:Savings",
    txn_ids=(1,),
):
    """A two-posting ProjectedEntry: bank leg + a contra (Expenses) leg.

    ``contra_amount`` is the *negative* contra posting (what Paisa's Debit Entry
    rule flags). The bank leg carries the equal-and-opposite positive amount so
    the entry is structurally balanced."""
    amount = Decimal(contra_amount)
    return ProjectedEntry(
        date=date,
        payee="Test",
        txn_ids=tuple(txn_ids),
        postings=(
            LedgerPosting(account=bank_account, amount=-amount),
            LedgerPosting(account=contra_account, amount=amount),
        ),
        kind=kind,
    )


def _debit_details(amount: str, account: str, date: datetime.date) -> str:
    """Render a details string in Paisa v0.7.4's exact ``ruleNonDebitAccount``
    template: ``<b>%.4f</b> got debited from <b>%s</b> on %s``."""
    amt = Decimal(amount).quantize(Decimal("0.0001"))
    return (
        f"<b>{amt:.4f}</b> got debited from <b>{account}</b> on "
        f"{date.strftime('%d %b %Y')}"
    )


def _diag(*issues: PaisaDiagnosisIssue) -> PaisaDiagnosis:
    danger = sum(1 for i in issues if i.level == "danger")
    warning = sum(1 for i in issues if i.level == "warning")
    first = next(
        ((i.summary or i.details) for i in issues if i.level == "danger"),
        None,
    )
    return PaisaDiagnosis(
        ok=danger == 0,
        danger_count=danger,
        warning_count=warning,
        issues=tuple(issues),
        first_message=first,
    )


def _danger(summary, details) -> PaisaDiagnosisIssue:
    return PaisaDiagnosisIssue(level="danger", summary=summary, details=details)


REFUND_DATE = datetime.date(2026, 2, 1)
CASHBACK_DATE = datetime.date(2026, 2, 2)
FEE_REV_DATE = datetime.date(2026, 2, 3)


# ---------------------------------------------------------------------------
# parse_debit_entry_details: real Paisa template
# ---------------------------------------------------------------------------


def test_parse_real_paisa_debit_entry_template():
    fp = parse_debit_entry_details(
        "<b>-236.0000</b> got debited from <b>Expenses:Fees Charges</b> on 15 Jan 2026"
    )
    assert fp == DebitEntryFingerprint(
        "Expenses:Fees Charges", datetime.date(2026, 1, 15), Decimal("-236.0000")
    )


def test_parse_handles_cashback_amount_and_spaces_in_account():
    fp = parse_debit_entry_details(
        "<b>-2916.1100</b> got debited from <b>Expenses:Cashback Rewards</b> on 02 Feb 2026"
    )
    assert fp.account == "Expenses:Cashback Rewards"
    assert fp.date == datetime.date(2026, 2, 2)
    assert fp.amount == Decimal("-2916.1100")


def test_parse_returns_none_for_non_matching_template():
    # A different danger kind's details, or a future upstream rewording, must
    # not half-match — the caller leaves the danger fatal.
    assert parse_debit_entry_details("Assets:Bank went negative") is None
    assert parse_debit_entry_details("") is None
    assert parse_debit_entry_details("garbage <b>not</b> a debit entry") is None
    assert (
        parse_debit_entry_details(
            "<b>-50.0000</b> posted to <b>Expenses:Refund</b>"  # wrong verb
        )
        is None
    )


def test_parse_returns_none_for_unparseable_amount_or_date():
    assert (
        parse_debit_entry_details(
            "<b>not-a-number</b> got debited from <b>Expenses:X</b> on 01 Feb 2026"
        )
        is None
    )
    assert (
        parse_debit_entry_details(
            "<b>-50.0000</b> got debited from <b>Expenses:X</b> on 99 Zzz 2026"
        )
        is None
    )


@pytest.mark.parametrize(
    "mon,expected",
    [
        ("Jan", 1),
        ("Feb", 2),
        ("Mar", 3),
        ("Apr", 4),
        ("May", 5),
        ("Jun", 6),
        ("Jul", 7),
        ("Aug", 8),
        ("Sep", 9),
        ("Oct", 10),
        ("Nov", 11),
        ("Dec", 12),
    ],
)
def test_parse_month_map_is_complete_and_locale_independent(mon, expected):
    """The month is parsed from a fixed English map (Go's ``time.Format`` always
    emits English), so a non-English server locale cannot mis-parse Paisa's
    date and turn an expected danger fatal."""
    fp = parse_debit_entry_details(
        f"<b>-50.0000</b> got debited from <b>Expenses:Refund</b> on 15 {mon} 2026"
    )
    assert fp is not None
    assert fp.date == datetime.date(2026, expected, 15)


