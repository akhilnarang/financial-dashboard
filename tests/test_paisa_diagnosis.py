def test_expected_fingerprints_collect_only_negative_expenses():
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        ),
        # A positive expense (normal spend) is NOT expected.
        _entry(
            contra_account="Expenses:Groceries",
            contra_amount="100.00",
            date=REFUND_DATE,
        ),
    ]
    fps = expected_debit_entry_fingerprints(entries)
    assert fps == Counter(
        [DebitEntryFingerprint("Expenses:Refund", REFUND_DATE, Decimal("-50.0000"))]
    )


def test_expected_fingerprints_are_multiplicity_aware():
    """Two identical refunds consume two fingerprints (not deduplicated)."""
    e = _entry(
        contra_account="Expenses:Refund",
        contra_amount="-50.00",
        date=REFUND_DATE,
        kind="contra_expense",
    )
    fps = expected_debit_entry_fingerprints([e, e])
    assert (
        fps[DebitEntryFingerprint("Expenses:Refund", REFUND_DATE, Decimal("-50.0000"))]
        == 2
    )


def test_expected_fingerprints_ignore_non_expenses_postings():
    # Card payments, self-transfers, openings post to liabilities/assets, never
    # to Expenses, so they contribute no expected Debit Entry danger.
    entry = ProjectedEntry(
        date=REFUND_DATE,
        payee="Card Payment",
        txn_ids=(1,),
        postings=(
            LedgerPosting("Liabilities:Card:Hdfc:Card", Decimal("5000.00")),
            LedgerPosting("Assets:Bank:Hdfc:Savings", Decimal("-5000.00")),
        ),
        kind="card_payment",
    )
    assert not expected_debit_entry_fingerprints([entry])


