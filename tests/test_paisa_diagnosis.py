def test_refund_cashback_and_fee_reversal_are_accepted():
    """The headline case: legitimate generated contra-expense postings (refund,
    cashback, fee reversal) are all downgraded to accepted; sync succeeds."""
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        ),
        _entry(
            contra_account="Expenses:Cashback Rewards",
            contra_amount="-2916.11",
            date=CASHBACK_DATE,
            kind="contra_expense",
        ),
        _entry(
            contra_account="Expenses:Fees Charges",
            contra_amount="-236.00",
            date=FEE_REV_DATE,
            kind="expense",  # a fee reversal nets against the original fee
        ),
    ]
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
        ),
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-2916.11", "Expenses:Cashback Rewards", CASHBACK_DATE),
        ),
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-236.00", "Expenses:Fees Charges", FEE_REV_DATE),
        ),
    )
    c = classify_diagnosis(diag, entries)
    assert c.expected_count == 3
    assert c.accepted_count == 3
    assert c.fatal_count == 0
    assert c.first_fatal_message is None
    # raw_danger_count reflects what Paisa actually reported.
    assert c.raw_danger_count == 3


def test_extra_same_fingerprint_beyond_expected_multiplicity_is_fatal():
    """A third danger for a fingerprint expected twice is unmatched and fatal —
    multiplicity is exact, not set-membership."""
    e = _entry(
        contra_account="Expenses:Refund",
        contra_amount="-50.00",
        date=REFUND_DATE,
        kind="contra_expense",
    )
    # Only ONE expected, but Paisa reports two identical dangers.
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
        ),
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
        ),
    )
    c = classify_diagnosis(diag, [e])
    assert c.expected_count == 1
    assert c.accepted_count == 1
    assert c.fatal_count == 1
    assert c.first_fatal_message is not None
    assert "Debit Entry" in c.first_fatal_message


def test_unmatched_debit_entry_from_operator_journal_is_fatal():
    """A Debit Entry whose fingerprint is NOT in the expected multiset (e.g. an
    operator-authored negative Expenses posting in their own include) stays
    fatal. It must never be blanket-suppressed."""
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-77.00", "Expenses:Hobbies", datetime.date(2026, 3, 1)),
        ),
    )
    c = classify_diagnosis(diag, [])
    assert c.accepted_count == 0
    assert c.fatal_count == 1


def test_negative_balance_is_always_fatal():
    """A Negative Balance danger is never classifiable — it stays fatal
    regardless of what the projection generated (the synth agent owns those)."""
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        )
    ]
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
        ),
        _danger(
            "Negative Balance",
            "<b>Assets:Bank:Hdfc:Savings</b> account went negative (-100.00) on 01 Feb 2026",
        ),
    )
    c = classify_diagnosis(diag, entries)
    assert c.accepted_count == 1
    assert c.fatal_count == 1
    # The fatal reason names the real problem (Negative Balance), not the
    # accepted contra-expense.
    assert "Negative Balance" in (c.first_fatal_message or "")


def test_malformed_debit_entry_is_fatal():
    """An unparseable Debit Entry (future upstream rewording) is not provably
    expected → fatal, never silently accepted."""
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        )
    ]
    diag = _diag(
        _danger(DEBIT_ENTRY_SUMMARY, "some new upstream message format"),
    )
    c = classify_diagnosis(diag, entries)
    assert c.accepted_count == 0
    assert c.fatal_count == 1


def test_warning_issues_never_affect_classification():
    """Warnings (Unit Price Mismatch, allocation gaps) are neither accepted nor
    fatal — they never affected sync and still do not."""
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        )
    ]
    diag = PaisaDiagnosis(
        ok=False,
        danger_count=1,
        warning_count=2,
        issues=(
            PaisaDiagnosisIssue(
                level="warning", summary="Unit Price Mismatch", details="..."
            ),
            _danger(
                DEBIT_ENTRY_SUMMARY,
                _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
            ),
            PaisaDiagnosisIssue(
                level="warning",
                summary="Asset Accounts missing from Allocation Target",
                details="...",
            ),
        ),
        first_message="Debit Entry",
    )
    c = classify_diagnosis(diag, entries)
    assert c.accepted_count == 1
    assert c.fatal_count == 0


def test_other_danger_kinds_are_fatal():
    """Credit Entry and Exchange Price Missing are real problems; never
    accepted even when we generated contra-expense postings."""
    entries = [
        _entry(
            contra_account="Expenses:Refund",
            contra_amount="-50.00",
            date=REFUND_DATE,
            kind="contra_expense",
        )
    ]
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-50.00", "Expenses:Refund", REFUND_DATE),
        ),
        _danger(
            "Credit Entry",
            "<b>100.0000</b> got credited to <b>Income:Salary</b> on 01 Feb 2026",
        ),
        _danger("Exchange Price Missing", "Exchange price ... is not specified"),
    )
    c = classify_diagnosis(diag, entries)
    assert c.accepted_count == 1
    assert c.fatal_count == 2


def test_clean_diagnosis_with_no_dangers():
    c = classify_diagnosis(
        PaisaDiagnosis(True, 0, 0, (), None),
        [
            _entry(
                contra_account="Expenses:Groceries",
                contra_amount="100.00",
                date=REFUND_DATE,
            )
        ],
    )
    # A positive expense generates no expected Debit Entry; clean diagnosis.
    assert c.expected_count == 0
    assert c.accepted_count == 0
    assert c.fatal_count == 0


def test_first_fatal_message_strips_html_and_includes_detail():
    """The reason message is HTML-stripped and bounded, so Paisa's ``<b>`` tags
    never reach the sync reason / audit error."""
    entries = []  # nothing expected → the Debit Entry is fatal
    diag = _diag(
        _danger(
            DEBIT_ENTRY_SUMMARY,
            _debit_details("-77.00", "Expenses:Hobbies", datetime.date(2026, 3, 1)),
        ),
    )
    c = classify_diagnosis(diag, entries)
    msg = c.first_fatal_message
    assert "<" not in msg
    assert ">" not in msg
    assert "Expenses:Hobbies" in msg
    assert "Debit Entry" in msg
